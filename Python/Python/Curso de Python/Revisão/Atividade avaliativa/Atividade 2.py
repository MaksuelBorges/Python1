"""Calcule a soma de dois números inteiros.
Calcule a diferença entre dois números inteiros.
Multiplique dois números de ponto flutuante.
Divida dois números inteiros e arredonde o resultado para o número inteiro mais próximo.
Calcule a potência de um número elevado a outro número.
"""
x = 5
y = 3

soma = x + y
difereca = x != y
multiplicacao = float(x * y)
dividir = round(x / y)
potencia = x ** y
print(f'Soma: {soma}', f'\nDiferença: {difereca}', f'\nMultiplicação: {multiplicacao}', f'\nDivisão com arredondamento: {dividir}', f'\nPotencia: {potencia}')
